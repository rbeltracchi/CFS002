    private void cargarFotoAlumno(int dni) {
        ImageIcon imageIcon;
        try {
            imageIcon = new ImageIcon(getClass().getResource("/fotos/" + dni + ".jpg"));
        } catch (Exception e) {
            imageIcon = new ImageIcon(getClass().getResource("/fotos/no_foto.jpg"));
        }
        Image image = imageIcon.getImage(); // transform it 
        Image newimg = image.getScaledInstance(160, 160, java.awt.Image.SCALE_SMOOTH); // scale it the smooth way  
        imageIcon = new ImageIcon(newimg);
        jLabel1.setIcon(imageIcon);
    }

    
    
