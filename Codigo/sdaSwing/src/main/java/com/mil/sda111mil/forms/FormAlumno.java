    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        if (txt_dni_a.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un DNI para el Alumno");
            txt_dni_a.requestFocus();
            return;
        }
        if (txt_nombre.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Nombre para el Alumno");
            txt_nombre.requestFocus();
            return;
        }
        if (txt_apellido.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Apellido para el Alumno");
            txt_apellido.requestFocus();
            return;
        }
        if (txt_domicilio.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Domicilio para el Alumno");
            txt_domicilio.requestFocus();
            return;
        }
        if (fechaNacimiento.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Fecha de Nacimiento para el Alumno");
            fechaNacimiento.requestFocus();
            return;
        }
        // Vacio todos los campos de texto limpio la lista de seleccion de curso y limpio el campo de la fecha
        Responsable respon = Service.crearResponsable(txt_responsable_apellido.getText(), txt_responsable_nombre.getText(),
                Integer.valueOf(txt_dni_r.getText()), Integer.valueOf(txt_responsable_telefono_area.getText()), Integer.valueOf(txt_responsable_telefono_numero.getText()));
        int sexoAlumno = cboSexo.getSelectedIndex();
        String genero = "M";
        if (sexoAlumno == 1) {
            genero = "F";
        }
        Service.crearAlumno(txt_apellido.getText(), txt_nombre.getText(), Integer.valueOf(txt_dni_a.getText()), txt_domicilio.getText(),
                cboCurso.getSelectedIndex() + 1, fechaNacimiento.getDate(), genero, respon);

        inhabilitar();
        cleanAlumnoForm();
        btn_guardar.setEnabled(false);
        btn_cancelar.setEnabled(true);
        JOptionPane.showMessageDialog(rootPane, "Los Datos Fueron Guardados Exitosamente");
        mostrarListaAlumnos();
        btn_guardar.setText("Guardar");
    }//GEN-LAST:event_btn_guardarActionPerformed

