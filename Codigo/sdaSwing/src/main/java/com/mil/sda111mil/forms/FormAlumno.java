    private void cargarFichaAlumno(String dni) {
        habilitarEdicion();
        String[] model = listarCursos();
        cboCurso.setModel(new javax.swing.DefaultComboBoxModel<>(model));

        Alumno elegido = Service.listarPorDni(dni);
        txt_dni_a.setText(dni);
        txt_nombre.setText(elegido.getNombre());
        txt_apellido.setText(elegido.getApellido());
        txt_domicilio.setText(elegido.getDireccion());
        cboCurso.setSelectedIndex(elegido.getCurso().getIdcurso() - 1);
        fechaNacimiento.setDate(elegido.getFechaNacimiento());

        txt_dni_r.setText("" + elegido.getResponsable().getDni());
        txt_responsable_nombre.setText(elegido.getResponsable().getNombre());
        txt_dni_r.setEditable(false);
        txt_responsable_apellido.setText(elegido.getResponsable().getApellido());
        List<Telefono> listTelefonos = elegido.getResponsable().getTelefonos();
        txt_responsable_telefono_area.setText(String.valueOf(listTelefonos.get(0).getNumero().getCodigoArea()));
        txt_responsable_telefono_numero.setText(String.valueOf(listTelefonos.get(0).getNumero().getNroTelefono()));

        if (elegido.getSexo().equals("M")) {
            cboSexo.getModel().setSelectedItem("Masculino");
        } else {
            cboSexo.getModel().setSelectedItem("Femenino");
        }

        cargarFotoAlumno(elegido.getDni());

    }

