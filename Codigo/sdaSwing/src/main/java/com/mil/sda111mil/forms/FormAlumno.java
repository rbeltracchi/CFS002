    private String[] listarCursos() {

        List<Curso> l = Service.listarCursos();
        String[] model = new String[l.size()];
        int i = 0;
        for (Curso curso : l) {
            model[i] = curso.getCurso() + "º " + curso.getDivision();
            i++;
        }
        return model;
    }

